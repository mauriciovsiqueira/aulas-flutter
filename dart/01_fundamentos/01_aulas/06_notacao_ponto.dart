void main() {
  // Notação de ponto é uma forma de acessar membros (propriedades e métodos) de um objeto em Dart. 
  // Ela é representada pelo operador "." (ponto) e é usada para acessar os membros de uma classe ou de um objeto específico.

  // Exemplo com String
  String nome = "Dart";
  // Acessando a propriedade 'length' da string
  print(nome.length); 
  // Acessando o método 'toUpperCase()' da string
  print(nome.toUpperCase()); 

  // Exemplo com List
  List<int> numeros = [1, 2, 3, 4, 5];
  // Acessando a propriedade 'length' da lista
  print(numeros.length); 
  // Acessando o método 'isEmpty' da lista
  print(numeros.isEmpty); 
  

}